package com.drivingbuddy.data.model;

public class User {
    private String id;
    private String name;
    private String email;
    private String profilePictureUrl;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getProfilePictureUrl() { 
        return profilePictureUrl; 
    }

}
